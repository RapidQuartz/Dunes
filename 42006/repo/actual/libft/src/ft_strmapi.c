#include "../libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;
	size_t			n;

	i = 0;
	n = ft_strlen(s);
	str = malloc(n + 1 * sizeof(char));
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	if (s[i] == '\0')
		str[i] = '\0';
	return (str);
}
